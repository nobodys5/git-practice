package practice;



public class Practice2 {

	public static void main(String[] args) {
		
//	if문 코드 학습
		boolean health = true;
		// if 문의 논리형 코드로 boolean을 사용하여 진실이냐 거짓이냐를 사용
		// 코드를 풀이하자면 만약 건강하고 싶으면 health를 하고 그렇지않다면 헬스를 하지마라 라는 풀이다.
		if(health) {
			System.out.println("건강하고 싶으면");
			
		}
		if(!health) {
			System.out.println("그렇지 않다면");
		}
	}
}
