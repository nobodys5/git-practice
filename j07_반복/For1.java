package j07_반복;

public class For1 {
	public static void main(String[] args) {
		System.out.println("for 전");
		for(int i = 0; i < 5; i++) {
			System.out.println(i);
		}
		System.out.println("for 후");
		
		/*
		 * <for문의 기본형태>
		 * for(초기치; 조건문; 증가치)	{
	 	 * 		실행할 문장a;
	 	 * 		실행항 문장b;
	 	 * 		...ㄹ
	 	 * }						
		 */
	}

}
